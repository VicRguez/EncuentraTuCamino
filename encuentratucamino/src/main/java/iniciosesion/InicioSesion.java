    private void registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarActionPerformed

        //comprueba que todos los campos esten rellenos
        if(comprueba_NuevoUsuario(usuario.getText(),new String(pwd.getPassword()),new String(pwd_verifica.getPassword()),email.getText())){
            int cod=logica.compruebaUsuario(usuario.getText(), new String(pwd.getPassword()), email.getText());
            if(cod==-1){
                Usuario us=new Usuario(-1,usuario.getText(), new String(pwd.getPassword()), email.getText());
                logica.crear_usuario(us);
                CardLayout name = (CardLayout) getContentPane().getLayout();
                name.next(getContentPane());
            }
            else{
                JOptionPane.showMessageDialog(null, "ya existe un usuario con ese nombre o con ese correo");
            }
            
        }
        
    }//GEN-LAST:event_registrarActionPerformed

