    private boolean comprueba_NuevoUsuario(String usuario,String contraseña,String contraseñaverifica,String email){
        boolean flag=true;
        //si este campo de texto esta vacio
        if(usuario.isBlank()||usuario.isEmpty()){
            flag=false;
            JOptionPane.showMessageDialog(null, "usuario vacio");
        }
        if(contraseña.equals(contraseñaverifica)){
            if((contraseña.length()<6)||(!contraseña.contains("."))){
                flag=false;
                JOptionPane.showMessageDialog(null, "contraseña no valida");
            }
        }
        else{
            flag=false;
            JOptionPane.showMessageDialog(null, "las dos contraseñas no son iguales");
        }
        if((!email.contains("@"))||(!email.contains(".com"))){
            flag=false;
            JOptionPane.showMessageDialog(null, "email no valido");
        }
        return flag;
    }
